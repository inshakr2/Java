public class StringApp {
    public static void main(String[] args) {

        System.out.println("Hello World!"); // String
        //System.out.println('Hello World'); Character
        System.out.println('H');

        System.out.println(
                "Hello" +
                "\n" + // new line
                "World");

        System.out.println(
                "Hello" +
                "\t" + // tab
                "\"World\""); // escape
    }
}
