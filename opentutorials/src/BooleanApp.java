public class BooleanApp {
    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;

        System.out.println(a && b);
        System.out.println(a | b);

        String c = "Hello Chany!";
        System.out.println(c.contains("Chany"));
        System.out.println(c.contains("World!"));

        System.out.println(1 > 1);
        System.out.println(1 == 1);
        System.out.println(1 >= 1);
        System.out.println(1 <= 1);

    }
}
